package com.kalos.beans;

import com.kalos.enumeraciones.Espiritu;

public class SubstractorPrefijosBean {

	private String prefijo;
	private String preposicion;
	private String agregarAlComer;
	private Espiritu espiritu;
	private int vocalAntes;
	
	/**
	 * @return Returns the agregarAlComer.
	 */
	public String getAgregarAlComer() {
		return agregarAlComer;
	}
	/**
	 * @param agregarAlComer The agregarAlComer to set.
	 */
	public void setAgregarAlComer(String agregarAlComer) {
		this.agregarAlComer = agregarAlComer;
	}
	/**
	 * @return Returns the espiritu.
	 */
	public Espiritu getEspiritu() {
		return espiritu;
	}
	/**
	 * @param espiritu The espiritu to set.
	 */
	public void setEspiritu(Espiritu espiritu) {
		this.espiritu = espiritu;
	}
	/**
	 * @return Returns the prefijo.
	 */
	public String getPrefijo() {
		return prefijo;
	}
	/**
	 * @param prefijo The prefijo to set.
	 */
	public void setPrefijo(String prefijo) {
		this.prefijo = prefijo;
	}
	/**
	 * @return Returns the preposicion.
	 */
	public String getPreposicion() {
		return preposicion;
	}
	/**
	 * @param preposicion The preposicion to set.
	 */
	public void setPreposicion(String preposicion) {
		this.preposicion = preposicion;
	}
	/**
	 * @return Returns the vocalAntes.
	 */
	public int getVocalAntes() {
		return vocalAntes;
	}
	/**
	 * @param vocalAntes The vocalAntes to set.
	 */
	public void setVocalAntes(int vocalAntes) {
		this.vocalAntes = vocalAntes;
	}

}
